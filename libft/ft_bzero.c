#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*sh;

	sh = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		sh[i] = 0;
		i++;
	}
}
